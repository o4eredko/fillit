#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	size_t	i;
	char	*s1;

	s1 = (char*)b;
	i = 0;
	while (i < n)
	{
		s1[i] = (char)c;
		i++;
	}
	b = s1;
	return (b);
}
