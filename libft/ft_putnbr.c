#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	if (n < 0)
		ft_putchar('-');
	i = 1;
	while (n / i > 9 || n / i < -9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar((char)(((n < 0) ? -(n / i) : n / i) + 48));
		n %= i;
		i /= 10;
	}
}
