int	ft_atoi(const char *str)
{
	long	i;
	int		m;
	long	res;

	i = 0;
	m = 1;
	res = 0;
	while (((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
			&& str[i] != '\0')
		i++;
	if (str[i] == '-')
		m = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		if (9223372036854775807 - res < str[i] - 48)
		{
			return (m == 1) ? -1 : 0;
		}
		res = res * 10 + str[i] - 48;
		i++;
	}
	return ((int)res * m);
}
