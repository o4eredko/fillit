#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int i;

	if (n < 0)
		ft_putchar_fd('-', fd);
	i = 1;
	while (n / i > 9 || n / i < -9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar_fd((char)(((n < 0) ? -(n / i) : n / i) + 48), fd);
		n %= i;
		i /= 10;
	}
}
